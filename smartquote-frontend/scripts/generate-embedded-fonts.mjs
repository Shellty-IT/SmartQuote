#!/usr/bin/env node
// scripts/generate-embedded-fonts.mjs
// Reads font WOFF2 files from @fontsource-variable packages and generates
// a TypeScript module with base64-encoded font data for use in Puppeteer-generated PDFs.
// Run: node scripts/generate-embedded-fonts.mjs
import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')

function toBase64(relPath) {
    const fullPath = resolve(root, 'node_modules', relPath)
    return readFileSync(fullPath).toString('base64')
}

const outfitNormal = toBase64('@fontsource-variable/outfit/files/outfit-latin-wght-normal.woff2')
const outfitLatinExt = toBase64('@fontsource-variable/outfit/files/outfit-latin-ext-wght-normal.woff2')
const ss3Normal = toBase64('@fontsource-variable/source-sans-3/files/source-sans-3-latin-wght-normal.woff2')
const ss3LatinExt = toBase64('@fontsource-variable/source-sans-3/files/source-sans-3-latin-ext-wght-normal.woff2')
const ss3Italic = toBase64('@fontsource-variable/source-sans-3/files/source-sans-3-latin-wght-italic.woff2')
const ss3ItalicLatinExt = toBase64('@fontsource-variable/source-sans-3/files/source-sans-3-latin-ext-wght-italic.woff2')

