#!/usr/bin/env node
// scripts/generate-embedded-fonts.mjs
// Generates src/lib/pdf/embedded-fonts.ts with base64-encoded static WOFF2 font data.
// Uses static (non-variable) fonts — variable fonts cause garbled text in Firefox PDF viewer.
// Run: node scripts/generate-embedded-fonts.mjs
import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')

const LATIN_EXT_RANGE = 'U+0100-02BA,U+02BD-02C5,U+02C7-02CC,U+02CE-02D7,U+02DD-02FF,U+0304,U+0308,U+0329,U+1D00-1DBF,U+1E00-1E9F,U+1EF2-1EFF,U+2020,U+20A0-20AB,U+20AD-20C0,U+2113,U+2C60-2C7F,U+A720-A7FF'
const LATIN_RANGE = 'U+0000-00FF,U+0131,U+0152-0153,U+02BB-02BC,U+02C6,U+02DA,U+02DC,U+0304,U+0308,U+0329,U+2000-206F,U+20AC,U+2122,U+2191,U+2193,U+2212,U+2215,U+FEFF,U+FFFD'

function b64(relPath) {
    return readFileSync(resolve(root, 'node_modules', relPath)).toString('base64')
}

function outfitFace(weight, subset, range) {
    const file = `@fontsource/outfit/files/outfit-${subset}-${weight}-normal.woff2`
    return `@font-face {
  font-family: 'Outfit';
  font-style: normal;
  font-display: swap;
  font-weight: ${weight};
  src: url('data:font/woff2;base64,${b64(file)}') format('woff2');
  unicode-range: ${range};
}`
}

function ss3Face(weight, style, subset, range) {
    const file = `@fontsource/source-sans-3/files/source-sans-3-${subset}-${weight}-${style}.woff2`
    return `@font-face {
  font-family: 'Source Sans 3';
  font-style: ${style};
  font-display: swap;
  font-weight: ${weight};
  src: url('data:font/woff2;base64,${b64(file)}') format('woff2');
  unicode-range: ${range};
}`
}

// Outfit weights used in proposal-html.ts: 300 400 500 600 700 800
const outfitWeights = [300, 400, 500, 600, 700, 800]

// Source Sans 3 weights used in contract-short-html.ts: 300 400 600 700 (normal + italic 400)
const ss3Weights = [300, 400, 600, 700]

const blocks = [
    '/* ── Outfit (latin-ext first — browser picks first matching unicode-range) ── */',
    ...outfitWeights.map(w => outfitFace(w, 'latin-ext', LATIN_EXT_RANGE)),
    '/* ── Outfit (latin) ── */',
    ...outfitWeights.map(w => outfitFace(w, 'latin', LATIN_RANGE)),
    '/* ── Source Sans 3 normal (latin-ext) ── */',
    ...ss3Weights.map(w => ss3Face(w, 'normal', 'latin-ext', LATIN_EXT_RANGE)),
    '/* ── Source Sans 3 normal (latin) ── */',
    ...ss3Weights.map(w => ss3Face(w, 'normal', 'latin', LATIN_RANGE)),
    '/* ── Source Sans 3 italic (latin-ext) ── */',
    ss3Face(400, 'italic', 'latin-ext', LATIN_EXT_RANGE),
    '/* ── Source Sans 3 italic (latin) ── */',
    ss3Face(400, 'italic', 'latin', LATIN_RANGE),
]

const css = blocks.join('\n\n') + '\n'

const tsContent = `// src/lib/pdf/embedded-fonts.ts
// Auto-generated by scripts/generate-embedded-fonts.mjs — do not edit manually.
// Static WOFF2 fonts (non-variable) for Puppeteer PDF generation.
// Variable fonts cause garbled text in Firefox PDF viewer — static fonts work in all viewers.
// Fonts: Outfit (300-800), Source Sans 3 (300-700 + italic 400), latin + latin-ext (Polish support).

export const EMBEDDED_FONTS_CSS = ${JSON.stringify(css)}
`

const outPath = resolve(root, 'src/lib/pdf/embedded-fonts.ts')
writeFileSync(outPath, tsContent, 'utf-8')

const kb = Math.round(tsContent.length / 1024)
console.log(`✅ Generated ${outPath} (${kb} KB)`)
console.log(`   Outfit weights: ${outfitWeights.join(', ')} × 2 subsets`)
console.log(`   Source Sans 3 weights: ${ss3Weights.join(', ')} normal × 2 subsets + 400 italic × 2 subsets`)
